# ─── Excel Report ────────────────────────────────────────────

def _generate_excel(args: dict, **kw) -> str:
    title = args.get("title", "CRYO Data Export")
    sheets = args.get("sheets", [])

    if not sheets:
        return json.dumps({"error": "Provide 'sheets' (list of {name, data: [{col: val}]})"})

    logger.info("Generating Excel: title=%r sheets=%d", title, len(sheets))

    try:
        from openpyxl import Workbook
        from openpyxl.styles import Font, PatternFill, Alignment, Border, Side

        filename, filepath = _gen_filename("data", "xlsx")
        wb = Workbook()

        # Style constants
        header_font = Font(name="Calibri", size=11, bold=True, color="00E5C7")
        header_fill = PatternFill(start_color="0F1419", end_color="0F1419", fill_type="solid")
        cell_font = Font(name="Calibri", size=10, color="E0E6ED")
        border = Border(
            bottom=Side(style="thin", color="2A3A4E"),
            right=Side(style="thin", color="2A3A4E"),
        )

        for i, sheet_data in enumerate(sheets):
            sheet_name = sheet_data.get("name", f"Sheet{i+1}")[:31]
            data = sheet_data.get("data", [])

            if i == 0:
                ws = wb.active
                ws.title = sheet_name
            else:
                ws = wb.create_sheet(sheet_name)

            if not data:
                continue

            # Headers
            if isinstance(data[0], dict):
                headers = list(data[0].keys())
                for col, header in enumerate(headers, 1):
                    cell = ws.cell(row=1, column=col, value=header)
                    cell.font = header_font
                    cell.fill = header_fill
                    cell.alignment = Alignment(horizontal="center")

                # Data rows
                for row_idx, row_data in enumerate(data[:10000], 2):
                    for col, header in enumerate(headers, 1):
                        val = row_data.get(header, "")
                        cell = ws.cell(row=row_idx, column=col, value=str(val) if not isinstance(val, (int, float)) else val)
                        cell.font = cell_font
                        cell.border = border

                # Auto-width columns
                for col in range(1, len(headers) + 1):
                    max_len = max(
                        len(str(ws.cell(row=r, column=col).value or ""))
                        for r in range(1, min(len(data) + 2, 50))
                    )
                    ws.column_dimensions[ws.cell(row=1, column=col).column_letter].width = min(max_len + 4, 50)

            # Freeze header row
            ws.freeze_panes = "A2"

        wb.save(str(filepath))

        logger.info("Excel generated: %s (%d bytes)", filename, filepath.stat().st_size)
        return json.dumps({
            "status": "success",
            "filename": filename,
            "path": str(filepath),
            "size_bytes": filepath.stat().st_size,
            "download_url": f"/api/reports/{filename}",
            "sheets": [s.get("name", f"Sheet{i+1}") for i, s in enumerate(sheets)],
            "total_rows": sum(len(s.get("data", [])) for s in sheets),
        })

    except ImportError:
        logger.error("openpyxl not installed")
        return json.dumps({"error": "openpyxl not installed. Run: pip install openpyxl"})
    except Exception as e:
        logger.error("Excel generation failed: %s", e, exc_info=True)
        return json.dumps({"error": f"Excel generation failed: {e}"})


