logger = logging.getLogger("cryo.reports")

REPORTS_DIR = Path(os.getenv("CRYO_REPORTS_DIR", "/tmp/cryo-reports"))
REPORTS_DIR.mkdir(parents=True, exist_ok=True)


def _gen_filename(prefix: str, ext: str) -> tuple[str, Path]:
    file_id = uuid.uuid4().hex[:12]
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    name = f"{prefix}_{ts}_{file_id}.{ext}"
    return name, REPORTS_DIR / name


# ─── PDF Report ──────────────────────────────────────────────

def _generate_pdf(args: dict, **kw) -> str:
    title = args.get("title", "CRYO Research Report")
    sections = args.get("sections", [])
    summary = args.get("summary", "")

    if not sections and not summary:
        return json.dumps({"error": "Provide 'sections' (list of {heading, content}) or 'summary' text"})

    logger.info("Generating PDF: title=%r sections=%d", title, len(sections))

    try:
        from reportlab.lib.pagesizes import A4
        from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
        from reportlab.lib.colors import HexColor
        from reportlab.lib.units import inch
        from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle, PageBreak
        from reportlab.lib.enums import TA_CENTER, TA_JUSTIFY

        filename, filepath = _gen_filename("report", "pdf")

        doc = SimpleDocTemplate(str(filepath), pagesize=A4,
                                leftMargin=0.8*inch, rightMargin=0.8*inch,
                                topMargin=0.8*inch, bottomMargin=0.8*inch)

        styles = getSampleStyleSheet()
        styles.add(ParagraphStyle(
            "CryoTitle", parent=styles["Title"],
            fontSize=24, textColor=HexColor("#00e5c7"),
            spaceAfter=20,
        ))
        styles.add(ParagraphStyle(
            "CryoHeading", parent=styles["Heading1"],
            fontSize=16, textColor=HexColor("#22d3ee"),
            spaceBefore=16, spaceAfter=8,
        ))
        styles.add(ParagraphStyle(
            "CryoBody", parent=styles["Normal"],
            fontSize=10, leading=14, alignment=TA_JUSTIFY,
            spaceAfter=8,
        ))
        styles.add(ParagraphStyle(
            "CryoMeta", parent=styles["Normal"],
            fontSize=8, textColor=HexColor("#6b7d93"),
        ))

        story = []

        # Title page
        story.append(Spacer(1, 1.5*inch))
        story.append(Paragraph(title, styles["CryoTitle"]))
        story.append(Spacer(1, 0.3*inch))
        story.append(Paragraph(
            f"Generated by CRYO — {datetime.now().strftime('%B %d, %Y at %H:%M')}",
            styles["CryoMeta"]
        ))
        story.append(Spacer(1, 0.5*inch))

        if summary:
            story.append(Paragraph("Executive Summary", styles["CryoHeading"]))
            story.append(Paragraph(summary, styles["CryoBody"]))
            story.append(Spacer(1, 0.3*inch))

        # Sections
        for section in sections:
            heading = section.get("heading", "Section")
            content = section.get("content", "")
            story.append(Paragraph(heading, styles["CryoHeading"]))

            # Handle content — could be text or table data
            if isinstance(content, list):
                # Table data: list of dicts
                if content and isinstance(content[0], dict):
                    headers = list(content[0].keys())
                    table_data = [headers]
                    for row in content[:50]:
                        table_data.append([str(row.get(h, ""))[:60] for h in headers])

                    t = Table(table_data, repeatRows=1)
                    t.setStyle(TableStyle([
                        ("BACKGROUND", (0, 0), (-1, 0), HexColor("#1c2430")),
                        ("TEXTCOLOR", (0, 0), (-1, 0), HexColor("#00e5c7")),
                        ("FONTSIZE", (0, 0), (-1, -1), 8),
                        ("GRID", (0, 0), (-1, -1), 0.5, HexColor("#2a3a4e")),
                        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [HexColor("#0f1419"), HexColor("#151b23")]),
                        ("TEXTCOLOR", (0, 1), (-1, -1), HexColor("#e0e6ed")),
                        ("VALIGN", (0, 0), (-1, -1), "TOP"),
                        ("WORDWRAP", (0, 0), (-1, -1), True),
                    ]))
                    story.append(t)
                else:
                    for item in content:
                        story.append(Paragraph(f"• {item}", styles["CryoBody"]))
            else:
                # Plain text — split into paragraphs
                for para in str(content).split("\n\n"):
                    if para.strip():
                        story.append(Paragraph(para.strip(), styles["CryoBody"]))

            story.append(Spacer(1, 0.2*inch))

        doc.build(story)

        logger.info("PDF generated: %s (%d bytes)", filename, filepath.stat().st_size)
        return json.dumps({
            "status": "success",
            "filename": filename,
            "path": str(filepath),
            "size_bytes": filepath.stat().st_size,
            "download_url": f"/api/reports/{filename}",
        })

    except ImportError:
        logger.error("reportlab not installed")
        return json.dumps({"error": "reportlab not installed. Run: pip install reportlab"})
    except Exception as e:
        logger.error("PDF generation failed: %s", e, exc_info=True)
        return json.dumps({"error": f"PDF generation failed: {e}"})


