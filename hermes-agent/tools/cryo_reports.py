# ─── Chart/Dashboard Generation ──────────────────────────────

def _generate_chart(args: dict, **kw) -> str:
    chart_type = args.get("chart_type", "bar")
    title = args.get("title", "Chart")
    data = args.get("data", {})
    x_label = args.get("x_label", "")
    y_label = args.get("y_label", "")

    if not data:
        return json.dumps({"error": "Provide 'data' with labels and values"})

    logger.info("Generating chart: type=%s title=%r", chart_type, title)

    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
        import matplotlib.ticker as mticker

        filename, filepath = _gen_filename("chart", "png")

        # CRYO dark theme
        plt.style.use("dark_background")
        fig, ax = plt.subplots(figsize=(10, 6), facecolor="#0a0e14")
        ax.set_facecolor("#0f1419")

        labels = data.get("labels", [])
        values = data.get("values", [])
        colors = data.get("colors", None)

        if not colors:
            cryo_colors = ["#00e5c7", "#22d3ee", "#34d399", "#a78bfa", "#fbbf24", "#f87171", "#60a5fa", "#fb923c"]
            colors = [cryo_colors[i % len(cryo_colors)] for i in range(len(labels))]

        if chart_type == "bar":
            bars = ax.bar(labels, values, color=colors, edgecolor="#1c2430", linewidth=0.5)
            ax.bar_label(bars, fmt="%.1f", fontsize=8, color="#e0e6ed")

        elif chart_type == "horizontal_bar":
            bars = ax.barh(labels, values, color=colors, edgecolor="#1c2430", linewidth=0.5)
            ax.bar_label(bars, fmt="%.1f", fontsize=8, color="#e0e6ed")

        elif chart_type == "pie":
            wedges, texts, autotexts = ax.pie(
                values, labels=labels, colors=colors, autopct="%1.1f%%",
                textprops={"fontsize": 9, "color": "#e0e6ed"},
                wedgeprops={"edgecolor": "#0a0e14", "linewidth": 1},
            )

        elif chart_type == "line":
            ax.plot(labels, values, color="#00e5c7", linewidth=2, marker="o", markersize=6, markerfacecolor="#22d3ee")
            ax.fill_between(range(len(labels)), values, alpha=0.1, color="#00e5c7")

        elif chart_type == "scatter":
            x_vals = data.get("x", labels)
            y_vals = data.get("y", values)
            sizes = data.get("sizes", [50] * len(x_vals))
            ax.scatter(x_vals, y_vals, s=sizes, c=colors[:len(x_vals)], alpha=0.7, edgecolors="#1c2430")

        elif chart_type == "heatmap":
            import numpy as np
            matrix = data.get("matrix", [])
            if matrix:
                im = ax.imshow(matrix, cmap="YlGnBu", aspect="auto")
                fig.colorbar(im, ax=ax, shrink=0.8)
                if labels:
                    ax.set_xticks(range(len(labels)))
                    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
                row_labels = data.get("row_labels", [])
                if row_labels:
                    ax.set_yticks(range(len(row_labels)))
                    ax.set_yticklabels(row_labels, fontsize=8)

        ax.set_title(title, fontsize=14, color="#00e5c7", pad=15, fontweight="bold")
        if x_label:
            ax.set_xlabel(x_label, fontsize=10, color="#6b7d93")
        if y_label:
            ax.set_ylabel(y_label, fontsize=10, color="#6b7d93")

        ax.tick_params(colors="#6b7d93", labelsize=9)
        for spine in ax.spines.values():
            spine.set_color("#2a3a4e")
        ax.grid(axis="y", alpha=0.15, color="#2a3a4e")

        plt.tight_layout()
        fig.savefig(str(filepath), dpi=150, bbox_inches="tight", facecolor="#0a0e14")
        plt.close(fig)

        logger.info("Chart generated: %s (%d bytes)", filename, filepath.stat().st_size)
        return json.dumps({
            "status": "success",
            "filename": filename,
            "path": str(filepath),
            "size_bytes": filepath.stat().st_size,
            "download_url": f"/api/reports/{filename}",
            "chart_type": chart_type,
        })

    except ImportError:
        logger.error("matplotlib not installed")
        return json.dumps({"error": "matplotlib not installed. Run: pip install matplotlib"})
    except Exception as e:
        logger.error("Chart generation failed: %s", e, exc_info=True)
        return json.dumps({"error": f"Chart generation failed: {e}"})


